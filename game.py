def list_of_items(items):
    """This function takes a list of items (see items.py for the definition) and
    returns a comma-separated list of item names (as a string). For example:

    >>> list_of_items([item_pen, item_handbook])
    'a pen, a student handbook'

    >>> list_of_items([item_id])
    'id card'

    >>> list_of_items([])
    ''

    >>> list_of_items([item_money, item_handbook, item_laptop])
    'money, a student handbook, laptop'

    """

    list_items = "" #This makes list_items an empty string
    for item in items: #Goes through items for item
        name = item["name"] #A new variable is created and is given the item name that was defined in the previous code
        list_items = list_items + name + ", " #Empty string plus the name from the previous line of code and then a comma at the end

    if len(list_items) > 2: #If the length of list_items is more than 2
        list_items = list_items[:-2] #Remove the end 2 characters (", ")

    return list_items
    pass


